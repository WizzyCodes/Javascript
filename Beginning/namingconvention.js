//Naming convention with var
var $myName = "Wisdom";
console.log($myName);

var _todayIsFriday = true;
console.log(_todayIsFriday);


//Naming convention with let
let $signing = "Wisdom Iberi";
console.log($signing);
let _myTotalStudents = "200";
console.log (parseInt(_myTotalStudents));


//Naming convention with const
const $newWig = "Expression";
console.log($newWig);
const _myFavoriteFood = null;
console.log(_myFavoriteFood);
const _newSigning = "Calafiori";
console.log(_newSigning);

